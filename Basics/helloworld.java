//This is the first program of java which shows different ways to print output
public class Helloworld {
	public static void main(String args[])
	{
		//after printing this the cursor will move to next line
		System.out.println("Hello World");

		//here, after printing the values curssor doesn't moves to next line
		System.out.print("null");

		//normal way of getting the cursor to nextline
		System.out.print("Hello world\n");
	}
}
